# Complex Numbers

# Python provides complex numbers, which are written as 1.0+2.0j.
# If val is an imaginary number, its real and imaginary parts
# can be accessed using dot notation as val.real and val.imag.

complex = 6 + 2j
print(complex.real)
print(complex.imag)

